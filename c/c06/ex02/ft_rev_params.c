#include<unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_rev(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	argc = argc - 1;
	while (argc > 0)
	{
		ft_rev(argv[argc]);
		ft_putchar('\n');
		argc--;
	}
}
